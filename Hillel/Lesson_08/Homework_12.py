# За допомогою функції filter() з котрежу слів відфільтрувати тільки ті, які є поліндромами (які однаково читаються в обидві сторони), регістр літер не враховувати.
#
# inputdata = ('Країна', 'шалаш', 'Летел', 'вертольот', 'УЧУ', 'мем', 'мова')

inputdata = ('Країна', 'шалаш', 'Летел', 'вертольот', 'УЧУ', 'мем', 'мова')
inputdata_02 = filter(lambda x: str(x[0:]).upper() == str(x[::-1]).upper(), inputdata)
print(list(inputdata_02))
